using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class MouseMovement : MonoBehaviour
{
    public float mouseSensitivity = 100f;

    float xRotation = 0f;
    float YRotation = 0f;

    void Start()
    {
        //Ýmleci ekranýn ortasýna kilitleyip görünmez hale getiriyoruz
        Cursor.lockState = CursorLockMode.Locked;
    }

    void Update()
    {
        
            float mouseX = Input.GetAxis("Mouse X") * mouseSensitivity * Time.deltaTime;
            float mouseY = Input.GetAxis("Mouse Y") * mouseSensitivity * Time.deltaTime;

            //x ekseni etrafýndaki dönüþü kontrol ediyoruz (Yukarý ve aþaðý bak)
            xRotation -= mouseY;

            //döndürmeyi sýkýþtýrýyoruz, böylece aþýrý dönmeyelim (gerçek hayatta olduðu gibi)
            xRotation = Mathf.Clamp(xRotation, -90f, 90f);

            //y ekseni etrafýndaki dönüþü kontrol ediyoruz (Yukarý ve aþaðý bak)
            YRotation += mouseX;

            //her iki rotasyonu da uyguluyoruz
            transform.localRotation = Quaternion.Euler(xRotation, YRotation, 0f);
        
        

    }
}
